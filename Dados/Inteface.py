@st.cache_data
def create_mock_view():
    """Recria a lógica da view vw_Cotacoes_Empresas no Pandas."""

    # 1. Faz o JOIN entre cotacoes_historicos (ch) e empresas (e)
    df_merged = pd.merge(df_cotacoes_historicos, df_empresas,
                         left_on='empresas_id_empresas', right_on='id_empresas',
                         how='inner', suffixes=('_cotacao', '_empresa'))

    # 2. Seleciona e renomeia as colunas conforme a definição da sua VIEW:
    # SELECT e.ticker, e.nome AS nome_empresa, ch.data_cotacao, ch.abertura, ch.fechamento, ch.maximo, ch.minimo, ch.volume
    df_view = df_merged[[
        'ticker', 'nome', 'data_cotacao', 'abertura', 'fechamento', 'maximo', 'minimo', 'volume'
    ]].rename(columns={'nome': 'nome_empresa'})

    # Converte a coluna de data para o tipo datetime e define como índice para o gráfico
    df_view['data_cotacao'] = pd.to_datetime(df_view['data_cotacao'])
    df_view.set_index('data_cotacao', inplace=True)

    return df_view


